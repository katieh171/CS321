    <h3>About the Viola:</h3>
    <p> The viola is an instrument belonging to the string family.
      Often it is mistaken for a violin, given that the sizes of the instruments are very similar, <br />though the viola is usually a little bigger to allow for tonal quality. <br />
      The strings are closer to those of a cello, rather than a violin. The viola and cello have the strings (from lowest to highest): C-G-D-A, while the violin has the strings: G-D-A-E.
      <br />The viola strings are an octave higher than the cello and one-fifth below the violin.</p>
